
a = [2, 45, 67, 3, 90, 22, 654]
print(a)

a.sort() #Sorts the list in asscending order
print(a)
a.reverse() #Reverses the list
print(a)
a.append(1024) #Appends/adds the element at the end of the list
print(a)
a.insert(2, 356) #Inserts 356 at the index 2
print(a)
a.pop(3) #Deletes the element at index 3
print(a)
a.remove(22) #Deletes 22 from the list
print(a)



